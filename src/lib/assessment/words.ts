/**
 * Asserts the word data is internally sound and never silently drifts from the
 * `tribes` source of truth. Throws on the first problem found:
 *  - a word mapped to no tribe, or
 *  - a word referencing a tribe slug that does not exist in `tribes`.
 *
 * Accepts an explicit list for testing; defaults to the real `words`.
 */
export function validateWordData(data: WordMapping[] = words): void {
  const validSlugs = new Set(tribes.map((t) => t.slug));
  for (const { word, tribes: mapped } of data) {
    if (mapped.length === 0) {
      throw new Error(`Word "${word}" is mapped to no tribe.`);
    }
    for (const slug of mapped) {
      if (!validSlugs.has(slug)) {
        throw new Error(
          `Word "${word}" references unknown tribe slug "${slug}".`,
        );
      }
    }
  }
}
